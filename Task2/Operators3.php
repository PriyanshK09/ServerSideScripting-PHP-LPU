<!-- Create a PHP script that demonstrates the use of increment and decrement operators.
 Start with a number, increment it, then decrement it, displaying the result after each operation -->

<?php
$a = readline("Enter the value of a: ");

// Increment
$a++;
echo("Value of a after increment is: " . $a . "\n");

// Decrement
$a--;
echo("Value of a after decrement is: " . $a . "\n");
?>